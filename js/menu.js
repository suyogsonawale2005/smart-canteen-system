let currentCategory = 'all';
let currentType = 'all';
let currentSearch = '';

function renderMenu(category = currentCategory, type = currentType, searchQuery = currentSearch) {
    currentCategory = category;
    currentType = type;
    currentSearch = searchQuery.toLowerCase();
    
    const menuGrid = document.getElementById('menu-grid');
    if (!menuGrid) return;
    
    // Clear old items
    menuGrid.innerHTML = '';
    
    const itemsToRender = window.menuItems || [];
    const filteredItems = itemsToRender.filter(item => {
        // 1. Search Filter
        const matchesSearch = item.name.toLowerCase().includes(currentSearch) || 
                              (item.desc && item.desc.toLowerCase().includes(currentSearch)) ||
                              (item.category && item.category.toLowerCase().includes(currentSearch));
        if (!matchesSearch) return false;

        // 2. Type Filter (Veg / Non-Veg)
        if (currentType !== 'all') {
            if (item.type !== currentType) return false;
        }

        // 3. Category Filter
        if (currentCategory === 'all') return true;
        
        // Map UI categories to database categories
        const catMap = {
            'cat-breakfast': ['Breakfast'],
            'cat-meals': ['Meals', 'Thali'],
            'cat-snacks': ['Snacks', 'Rolls', 'Starters'],
            'cat-beverages': ['Beverages', 'Cold Drink'],
            'cat-chinese': ['Chinese'],
            'cat-fastfood': ['Burgers', 'Pizza']
        };

        if (catMap[currentCategory]) {
            return catMap[currentCategory].some(c => item.category && item.category.toLowerCase().includes(c.toLowerCase()));
        }

        return true;
    });

    filteredItems.forEach((item, index) => {
        const outOfStock = window.isOutOfStock && window.isOutOfStock(item.name);
        
        const badgeHTML = item.type === 'veg' 
            ? `<span class="food-badge"><i class="fa-solid fa-leaf"></i> Veg</span>` 
            : `<span class="food-badge non-veg"><i class="fa-solid fa-drumstick-bite"></i> Non-Veg</span>`;

        // Image URL: from item.img if available and valid
        let dynamicImgUrl = item.img;
        
        if (!dynamicImgUrl || dynamicImgUrl.trim() === '' || dynamicImgUrl === 'null') {
            // Default images by category if missing
            const cat = (item.category || '').toLowerCase();
            if (cat.includes('beverage') || cat.includes('drink')) dynamicImgUrl = 'https://images.unsplash.com/photo-1544145945-f90425340c7e?auto=format&fit=crop&w=500&q=80';
            else if (cat.includes('snack') || cat.includes('starter')) dynamicImgUrl = 'https://images.unsplash.com/photo-1626074353765-517a681e40be?auto=format&fit=crop&w=500&q=80';
            else if (cat.includes('breakfast')) dynamicImgUrl = 'https://images.unsplash.com/photo-1533089860892-a7c6f0a88666?auto=format&fit=crop&w=500&q=80';
            else if (cat.includes('meal') || cat.includes('lunch')) dynamicImgUrl = 'https://images.unsplash.com/photo-1546833999-b9f581a1996d?auto=format&fit=crop&w=500&q=80';
            else if (cat.includes('burger') || cat.includes('pizza') || cat.includes('fast food')) dynamicImgUrl = 'https://images.unsplash.com/photo-1550547660-d9450f859349?auto=format&fit=crop&w=500&q=80';
            else dynamicImgUrl = 'https://images.unsplash.com/photo-1546069901-ba9599a7e63c?auto=format&fit=crop&w=500&q=80'; // generic food
        }

        const card = document.createElement('div');
        card.className = `food-card animate-fade-in delay-${(index % 3) + 1} ${outOfStock ? 'out-of-stock' : ''}`;
        
        const buttonHTML = outOfStock 
            ? `<span style="color: var(--danger); font-size: 0.8rem; font-weight: 700;">SOLD OUT</span>`
            : `<button class="btn-add" onclick="window.addToCart('${item.name}', ${item.price}, '${dynamicImgUrl}', ${item.id})" aria-label="Add to cart">
                <i class="fa-solid fa-plus"></i>
              </button>`;

        card.innerHTML = `
            <div class="food-img-wrapper" style="${outOfStock ? 'filter: grayscale(1); opacity: 0.6;' : ''}">
                ${badgeHTML}
                <img src="${dynamicImgUrl}" alt="${item.name}" loading="lazy" onerror="this.onerror=null;this.src='https://images.unsplash.com/photo-1546069901-ba9599a7e63c?auto=format&fit=crop&w=500&q=80';">
            </div>
            <div class="food-details">
                <h3 class="food-title">${item.name}</h3>
                <p class="food-desc">${item.desc}</p>
                <div class="food-footer">
                    <span class="food-price">₹${item.price.toFixed(2)}</span>
                    ${buttonHTML}
                </div>
            </div>
        `;
        menuGrid.appendChild(card);
    });
}

function initCategoryFilters() {
    const filterBtns = document.querySelectorAll('.category-btn');
    filterBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            filterBtns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            const cat = btn.getAttribute('data-cat');
            renderMenu(cat, currentType, currentSearch);
        });
    });

    const typeBtns = document.querySelectorAll('.type-btn');
    typeBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const isActive = btn.classList.contains('active-type');

            // Reset all type buttons first
            typeBtns.forEach(b => {
                b.classList.remove('active-type');
                b.style.background = 'transparent';
                b.style.color = 'var(--text-muted)';
            });

            if (isActive) {
                // Was active, now toggling off: Show all
                renderMenu(currentCategory, 'all', currentSearch);
            } else {
                // Activating a new type filter
                btn.classList.add('active-type');
                btn.style.background = 'var(--primary)';
                btn.style.color = 'white';
                const typeValue = btn.getAttribute('data-type');
                renderMenu(currentCategory, typeValue, currentSearch);
            }
        });
    });

    // Initialize Search Listener
    const searchInput = document.getElementById('menu-search');
    if (searchInput) {
        searchInput.addEventListener('input', (e) => {
            renderMenu(currentCategory, currentType, e.target.value);
        });
    }
}

// Ensure execution waits until DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    initCategoryFilters();
    
    // Fetch menu from the backend API
    fetch('https://smart-canteen-system-hvah.onrender.com/api/menu')
        .then(res => res.json())
        .then(data => {
            if (data.menu) {
                window.menuItems = data.menu.map(item => ({
                    id: item.item_id,
                    name: item.item_name,
                    price: item.price,
                    type: (item.type || 'veg').toLowerCase(),
                    category: item.category,
                    img: item.image_url,
                    desc: item.item_name + ' - ' + (item.category || 'Delicious food')
                }));
            } else {
                console.error("Failed to load menu data", data);
            }
            renderMenu(currentCategory, currentType, currentSearch);
        })
        .catch(err => {
            console.error('Error fetching menu:', err);
            // Fallback to local storage if API fails
            const liveMenu = localStorage.getItem('canteen_menu');
            if (liveMenu) {
                window.menuItems = JSON.parse(liveMenu);
            }
            renderMenu(currentCategory, currentType, currentSearch);
        });
});